package exercicioObjeto;

public class Aviao {
	
	public String nomeCompanhia;
	public String nomeAeronave;
	public String cor;
	public int tamanho;
	
	public Aviao() {
		
	}

	public Aviao(String nomeCompanhia, String nomeAeronave, String cor, int tamanho) {
		super();
		this.nomeCompanhia = nomeCompanhia;
		this.nomeAeronave = nomeAeronave;
		this.cor = cor;
		this.tamanho = tamanho;
	}
}
